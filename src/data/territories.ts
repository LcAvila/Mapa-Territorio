export const allTerritories: TerritoryAssignment[] = [...planejamentoRJ, ...atendimentoRJ];
export const allBairros: BairroAssignment[] = [...planejamentoBairros, ...atendimentoBairros];

export function getMunicipioResponsaveis(
  municipio: string,
  uf: string,
  modo: "planejamento" | "atendimento"
): string[] {
  return allTerritories
    .filter(t => 
      t.municipio.toLowerCase() === municipio.toLowerCase() && 
      t.uf === uf && 
      t.modo === modo
    )
    .map(t => t.repCode);
}

export function getBairroResponsaveis(
  bairro: string,
  municipio: string,
  uf: string,
  modo: "planejamento" | "atendimento"
): string[] {
  return allBairros
    .filter(b => 
      b.bairro.toLowerCase() === bairro.toLowerCase() && 
      b.municipio.toLowerCase() === municipio.toLowerCase() && 
      b.uf === uf && 
      b.modo === modo
    )
    .map(b => b.repCode);
}

export function hasBairros(municipio: string, uf: string): boolean {
  return allBairros.some(b => 
    b.municipio.toLowerCase() === municipio.toLowerCase() && b.uf === uf
  );
}
